    private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
        if(logoSelec) {
            contenido.add(new LogoSistemas().escalar(0.1, 0.1).trasladar(evt.getX(), evt.getY()));
            paint(this.getGraphics());
        }
        else if(textoSelec) {
            String txt = JOptionPane.showInputDialog("Escribir texto:");
            contenido.add(new TextoGrafico(txt).trasladar(evt.getX(), evt.getY()));
            paint(this.getGraphics());
        }
    }//GEN-LAST:event_formMouseClicked

    private void btnMascotaAccion(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMascotaAccion
        MultiDibujable d = new Dino().escalar(5, 5);
        MultiDibujable x = d.trasladar(-d.minX(), -d.minY() + 200) ;
        contenido.add(x);
        x.pasos = true;
        paint(this.getGraphics());
    }//GEN-LAST:event_btnMascotaAccion

    /**
     * @param args the command line arguments
     */
    private boolean logoSelec = false;
    private boolean textoSelec = false;
    
    public void paint(Graphics g){
        super.paint(g);
        textoSelec = logoSelec = false;
        for(Dibujable d : contenido)
            d.dibujar(g);
    }

